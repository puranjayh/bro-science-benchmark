def main():
    rng = random.Random(7)
    items = []
    for m in MYTHS:
        held = m.get("held_out", False)
        for i, q in enumerate(m["q"]):
            opts = [("correct", m["correct"]), ("bro", m["bro"])] + [("d", d) for d in m["d"]]
            rng.shuffle(opts)
            letters = [chr(65 + j) for j in range(len(opts))]
            options = {letters[j]: opts[j][1] for j in range(len(opts))}
            answer = letters[[o[0] for o in opts].index("correct")]
            bro = letters[[o[0] for o in opts].index("bro")]
            items.append(dict(
                id=f"{m['key']}_{i}", category=m["cat"], myth=m["key"],
                held_out=held, hard=m.get("hard", False), question=q, options=options,
                answer=answer, bro_science_option=bro,
            ))

    out = dict(
        name="BroScienceBench", version="1.0",
        description="Myth-vs-evidence MC benchmark for LLM strength-training advice. ~120 items, "
                    "incl. adversarial phrasings, calibration (overclaiming) items, and held-out myths "
                    "(not named in ALI's guardrails) to test generalization vs pattern-matching.",
        grading="exact-match on option letter. accuracy=chose answer; myth_adherence=chose bro_science_option. "
                "For calibration items, bro_science_option = the overclaim.",
        ablation_conditions=["baseline", "+knowledge", "+guardrails", "+rag", "full"],
        n_items=len(items), n_held_out=sum(1 for it in items if it["held_out"]),
        items=items,
    )
    p = pathlib.Path(__file__).parent / "broscience_bench_v1.json"
    json.dump(out, open(p, "w"), indent=2)
    print(f"wrote {len(items)} items ({out['n_held_out']} held-out) -> {p}")


if __name__ == "__main__":
    main()
